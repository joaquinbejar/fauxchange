        // The chain synthesis is a pure function of (config, base) — no clock, no
        // RNG — so two calls produce the byte-identical chain (the deterministic
        // half of the same-seed acceptance line; the stochastic price path is
        // reproduced from the journal, not regenerated here).
        let config = base_config().with_smile_curve(0.7).with_skew_slope(-0.2);
