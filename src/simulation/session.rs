    #[test]
    fn test_civil_from_days_matches_known_dates() {
        // 2025-01-01 is 20089 days after the Unix epoch.
        assert_eq!(civil_from_days(20_089), (2025, 1, 1));
        // The epoch itself.
        assert_eq!(civil_from_days(0), (1970, 1, 1));
        // A leap day.
        assert_eq!(civil_from_days(civil_days(2024, 2, 29)), (2024, 2, 29));
    }

    /// Test helper: days since the epoch for a civil date (inverse of
    /// `civil_from_days`), for the leap-day assertion.
    fn civil_days(y: i64, m: u32, d: u32) -> i64 {
        let y = if m <= 2 { y - 1 } else { y };
        let era = if y >= 0 { y } else { y - 399 } / 400;
        let yoe = y - era * 400;
        let mp = (if m > 2 { m - 3 } else { m + 9 }) as i64;
        let doy = (153 * mp + 2) / 5 + d as i64 - 1;
        let doe = yoe * 365 + yoe / 4 - yoe / 100 + doy;
        era * 146_097 + doe - 719_468
    }
