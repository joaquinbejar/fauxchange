/// Milliseconds in a UTC day — the civil-date arithmetic basis (fixed, never a
/// wall-clock read).
const MILLIS_PER_DAY: u64 = 24 * 60 * 60 * 1_000;

/// The default number of strikes in a synthesised chain (an odd count so the ATM
/// strike is centred).
pub const DEFAULT_CHAIN_SIZE: usize = 11;

/// The default per-session `smile_curve` (`0.0` — a flat surface until a scenario
/// shapes it).
pub const DEFAULT_SMILE_CURVE: f64 = 0.0;

/// The default per-session `skew_slope` (`0.0` — no tilt).
pub const DEFAULT_SKEW_SLOPE: f64 = 0.0;

/// The default per-session chain `spread` fraction (`2%`).
pub const DEFAULT_SPREAD: f64 = 0.02;

/// The default per-session dividend yield (`0%`).
pub const DEFAULT_DIVIDEND_YIELD: f64 = 0.0;

/// The default risk-free rate (`5%`), matching the market-maker pricer default.
pub const DEFAULT_RISK_FREE_RATE: f64 = 0.05;

/// The fraction of spot the default strike interval targets (`1%` of spot),
/// floored at `1` whole unit — a sensible grid step across price scales.
const DEFAULT_STRIKE_INTERVAL_FRACTION: f64 = 0.01;

