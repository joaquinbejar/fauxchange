//! Stepped synthetic sessions — the OptionChain-Simulator model folded into
//! `fauxchange` as **code, not a dependency**: deterministic chain synthesis
//! (`expirations × strikes` with a `smile_curve`-shaped volatility surface),
//! per-session generation parameters, and step-by-step advancement on the stepped
//! venue clock ([031](../../milestones/v0.3-replay/031-stepped-synthetic-sessions.md),
//! [04 §3](../../docs/04-market-data-and-replay.md#3-chain-synthesis-and-stepped-sessions),
//! [ADR-0001](../../docs/adr/0001-consolidate-option-chain-orderbook-backend.md)).
//!
//! The separate service is **retired**: no ClickHouse / Redis / MongoDB stack and
//! no `/api/v1/chain` session-lifecycle REST surface come with it — chains already
//! live in the consolidated REST hierarchy. What is absorbed is the *idea*:
//!
//! - **Per-session generation parameters** ([`SessionConfig`]) — initial price,
//!   days-to-expiration, volatility, risk-free rate, dividend yield, walk method,
//!   strike interval, chain size, `smile_curve` (+ `skew_slope`), and `spread` —
//!   shape a synthetic chain per scenario.
//! - **Chain synthesis** ([`synthesize_chain`]) — materialise the
//!   `expirations × strikes` instrument grid deterministically, with the per-strike
//!   implied volatility shaped by the smile **through `optionstratlib`**
//!   ([`optionstratlib::chains::utils::adjust_volatility`], never a hand-rolled
//!   volatility surface): `σ(K) = σ_atm · clamp(1 + skew·m + smile·m²)`,
//!   `m = ln(K/S)`, so a positive `smile_curve` raises the wing IVs (the classic
//!   volatility smile).
//! - **Stepped advancement** — a session step advances the venue clock by the fixed
//!   virtual interval and walks the underlying one price step; both enter the
//!   sequencer as journaled `Clock` / `SimStep` commands, and the market maker's
//!   derived requotes are themselves journaled `AddOrder`s. Replay reproduces the
//!   session from the journal with the live requote engine muted (#030) — the
//!   application wiring (`AppState::materialize_session` / `AppState::step_session`)
//!   composes this domain synthesis with the #016 simulator + #028 clock + #015
//!   market maker.
//!
//! ## Determinism, scoped honestly (rule 5)
//!
//! The chain **synthesis** (the strike grid, the smile-shaped IVs, the DateTime
//! expiry, the instrument set) is a **pure deterministic function of the
//! [`SessionConfig`]** — no clock, no RNG — so the same config yields the identical
//! chain every time. The stepped **price path**, however, is generated by the
//! `optionstratlib` walk sampler, which builds its **own** `rand::rng()` per draw
//! and cannot consume the run seed
//! ([04 §2](../../docs/04-market-data-and-replay.md#2-synthetic-price-generation)):
//! the guaranteed reproduction of the *path* is the **journal** (replay
//! re-executes the recorded `SimStep`s), **not** seed-regeneration. So "a stepped
//! session advances identically for the same seed" holds precisely as: **identical
//! deterministic synthesis** for the same config, and **journal-driven** identical
//! replay of the stochastic price path — never a claim of a seed-regenerated price
//! path.
//!
//! ## Replay-stable expiries (rule / #032)
//!
//! Every synthesised expiry is an absolute [`ExpirationDate::DateTime`], derived by
//! **pure civil-date arithmetic** from a fixed base instant plus `days_to_expiration`
//! (never `Utc::now()`, never a `Days`-relative expiry), so a stepped session is
//! replay-stable.

