/// One synthesised strike: its whole-unit strike, the call/put contract symbols,
/// and the `smile_curve`-shaped implied volatility (one IV per strike, shared by the
/// call and put leg).
#[derive(Debug, Clone, PartialEq)]
pub struct SynthesizedStrike {
    /// The strike in **whole units**.
    pub strike: u64,
    /// The canonical call contract symbol.
    pub call: Symbol,
    /// The canonical put contract symbol.
    pub put: Symbol,
    /// The smile-shaped implied volatility at this strike (from `optionstratlib`).
    pub iv: f64,
}

/// A deterministically synthesised option chain — the `expirations × strikes`
/// instrument grid for one session, at one absolute [`ExpirationDate::DateTime`]
/// expiry, with per-strike smile IVs.
#[derive(Debug, Clone, PartialEq)]
pub struct SynthesizedChain {
    /// The underlying ticker.
    pub underlying: String,
    /// The absolute (canonical `DateTime`) expiry every contract is keyed on.
    pub expiration: ExpirationDate,
    /// The opening underlying price, in **cents**.
    pub spot: Cents,
    /// The materialised strikes, in ascending strike order.
    pub strikes: Vec<SynthesizedStrike>,
}

impl SynthesizedChain {
    /// The `(symbol, iv)` pairs for every leg (call then put per strike, ascending)
    /// — the instrument set to register with the market maker so each leaf is
    /// seeded with a smile-shaped quote.
    #[must_use]
    pub fn instruments(&self) -> Vec<(Symbol, f64)> {
        let mut out = Vec::with_capacity(self.strikes.len() * 2);
        for strike in &self.strikes {
            out.push((strike.call.clone(), strike.iv));
            out.push((strike.put.clone(), strike.iv));
        }
        out
    }

    /// The number of contracts materialised (two per strike).
    #[must_use]
    pub fn contract_count(&self) -> usize {
        self.strikes.len() * 2
    }
}

