/// The per-session generation parameters that shape a synthetic chain per scenario
/// ([04 §3](../../docs/04-market-data-and-replay.md#3-chain-synthesis-and-stepped-sessions)).
///
/// Serde with `deny_unknown_fields` so it is config-surface-ready (a stray field is
/// a hard decode error, never a silent typo). Money is integer [`Cents`]; the model
/// floats (`volatility`, `smile_curve`, …) are the documented analytic exception.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct SessionConfig {
    /// The underlying ticker the session drives (e.g. `"BTC"`).
    pub underlying: String,
    /// The opening / initial underlying price, in **cents**.
    pub initial_price: Cents,
    /// Days to expiration for the synthesised expiry (`> 0`).
    pub days_to_expiration: f64,
    /// The at-the-money base implied volatility (`> 0`, e.g. `0.20` for 20%).
    pub volatility: f64,
    /// The annualised risk-free rate (e.g. `0.05`).
    pub risk_free_rate: f64,
    /// The dividend yield (`>= 0`).
    pub dividend_yield: f64,
    /// The price-walk method, routed through [`WalkTypeConfig`] →
    /// `optionstratlib::WalkType`.
    pub walk_type: WalkTypeConfig,
    /// The gap between adjacent strikes, in **whole units** (`>= 1`).
    pub strike_interval: u64,
    /// The number of strikes to materialise (`>= 1`).
    pub chain_size: usize,
    /// The volatility-smile **curvature** term (`smile_curve`): `> 0` raises the
    /// wings (a smile), `< 0` lowers them, `0` is flat.
    pub smile_curve: f64,
    /// The volatility-smile **skew** term (slope per unit log-moneyness): tilts the
    /// surface. `0` is symmetric.
    pub skew_slope: f64,
    /// The chain spread fraction (`> 0`) — a **recorded scenario parameter** that
    /// shapes the synthesised chain's nominal bid/ask width. Note the venue seeds
    /// live liquidity through the **market maker** (whose two-sided quote spread is
    /// its own `base_spread_bps`), so this value is carried as part of the
    /// reproducible session descriptor rather than driving the sequenced quotes; a
    /// per-session MM spread is a documented seam (like the per-underlying persona
    /// knobs), deferred to the microstructure work (v0.5).
    pub spread: f64,
}

impl SessionConfig {
    /// Builds a session config with the grid / smile / rate parameters defaulted,
    /// deriving a `1%`-of-spot strike interval. Override the rest with the
    /// `with_*` setters.
    #[must_use]
    pub fn new(
        underlying: impl Into<String>,
        initial_price: Cents,
        days_to_expiration: f64,
        volatility: f64,
        walk_type: WalkTypeConfig,
    ) -> Self {
        let strike_interval = default_strike_interval(initial_price);
        Self {
            underlying: underlying.into(),
            initial_price,
            days_to_expiration,
            volatility,
            risk_free_rate: DEFAULT_RISK_FREE_RATE,
            dividend_yield: DEFAULT_DIVIDEND_YIELD,
            walk_type,
            strike_interval,
            chain_size: DEFAULT_CHAIN_SIZE,
            smile_curve: DEFAULT_SMILE_CURVE,
            skew_slope: DEFAULT_SKEW_SLOPE,
            spread: DEFAULT_SPREAD,
        }
    }

    /// Sets the strike interval (whole units).
    #[must_use]
    pub fn with_strike_interval(mut self, strike_interval: u64) -> Self {
        self.strike_interval = strike_interval;
        self
    }

    /// Sets the number of strikes to materialise.
    #[must_use]
    pub fn with_chain_size(mut self, chain_size: usize) -> Self {
        self.chain_size = chain_size;
        self
    }

    /// Sets the smile curvature term.
    #[must_use]
    pub fn with_smile_curve(mut self, smile_curve: f64) -> Self {
        self.smile_curve = smile_curve;
        self
    }

    /// Sets the smile skew slope.
    #[must_use]
    pub fn with_skew_slope(mut self, skew_slope: f64) -> Self {
        self.skew_slope = skew_slope;
        self
    }

    /// Sets the risk-free rate.
    #[must_use]
    pub fn with_risk_free_rate(mut self, risk_free_rate: f64) -> Self {
        self.risk_free_rate = risk_free_rate;
        self
    }

    /// Sets the dividend yield.
    #[must_use]
    pub fn with_dividend_yield(mut self, dividend_yield: f64) -> Self {
        self.dividend_yield = dividend_yield;
        self
    }

    /// Sets the chain spread fraction.
    #[must_use]
    pub fn with_spread(mut self, spread: f64) -> Self {
        self.spread = spread;
        self
    }

    /// Validates the session parameters at the `f64` boundary before synthesis.
    ///
    /// # Errors
    ///
    /// [`SimError::InvalidParameter`] naming the offending parameter if any value
    /// is non-finite or out of its documented range.
    pub fn validate(&self) -> Result<(), SimError> {
        if self.underlying.trim().is_empty() {
            return Err(SimError::InvalidParameter(
                "underlying must not be empty".into(),
            ));
        }
        if self.initial_price.get() == 0 {
            return Err(SimError::InvalidParameter(
                "initial_price must be > 0".into(),
            ));
        }
        require_finite_positive("days_to_expiration", self.days_to_expiration)?;
        require_finite_positive("volatility", self.volatility)?;
        require_finite("risk_free_rate", self.risk_free_rate)?;
        require_finite_nonneg("dividend_yield", self.dividend_yield)?;
        require_finite("smile_curve", self.smile_curve)?;
        require_finite("skew_slope", self.skew_slope)?;
        require_finite_positive("spread", self.spread)?;
        if self.strike_interval == 0 {
            return Err(SimError::InvalidParameter(
                "strike_interval must be >= 1".into(),
            ));
        }
        if self.chain_size == 0 {
            return Err(SimError::InvalidParameter("chain_size must be >= 1".into()));
        }
        Ok(())
    }

    /// The [`AssetConfig`] the session's underlying is hosted as on the price
    /// simulator (the walk seam the stepped path advances). The drift is `0.0`
    /// (the session shapes the level, not a directional drift).
    #[must_use]
    pub fn to_asset_config(&self) -> AssetConfig {
        AssetConfig::new(
            self.underlying.clone(),
            self.initial_price,
            self.volatility,
            self.walk_type,
        )
    }
}

/// The default strike interval: `~1%` of spot in whole units, floored at `1`.
fn default_strike_interval(initial_price: Cents) -> u64 {
    let spot_dollars = initial_price.get() as f64 / 100.0;
    let interval = (spot_dollars * DEFAULT_STRIKE_INTERVAL_FRACTION).round();
    if interval.is_finite() && interval >= 1.0 {
        interval as u64
    } else {
        1
    }
}

